package com.example.springboot.multiplication;

/**
 * The multiplication class
 */
public class Multiplication {
	
	private int argA;
	private int argB;
	
	//the result of the calculation
	private int result;

	public Multiplication(int argA, int argB) {
		super();
		this.argA = argA;
		this.argB = argB;
		this.result = argA * argB;
	}

	//the constructor

}
